export type Locale = "zh-CN" | "en-US";

const localeStorageKey = "mistvault.locale";

export const supportedLocales: { value: Locale; label: string }[] = [
  { value: "zh-CN", label: "中文" },
  { value: "en-US", label: "English" }
];

export const isLocale = (value: string | null): value is Locale =>
  value === "zh-CN" || value === "en-US";

export const readStoredLocale = (): Locale => {
  try {
    const stored = window.localStorage.getItem(localeStorageKey);
    return isLocale(stored) ? stored : "zh-CN";
  } catch {
    return "zh-CN";
  }
};

export const storeLocale = (locale: Locale) => {
  try {
    window.localStorage.setItem(localeStorageKey, locale);
  } catch {
    // Language preference is helpful but non-critical.
  }
};

